# Learning Python Functions

def greet(name):
    """Greet a person by their name"""
    return f"Hello, {name}!"

def add(a, b):
    """Add two numbers"""
    return a + b

def multiply(a, b):
    """Multiply two numbers"""
    return a * b

# Test functions
print(greet("Surya"))
print(f"Sum: {add(5, 3)}")
print(f"Product: {multiply(4, 7)}")
